    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            int  res=MySecurityController.addNewAccount(userNameText.getText(),passwordText.getText());
            if (res==-1) {
                JOptionPane.showMessageDialog(Account.this,"User Name already exists");
            }else if (res>0) {
                JOptionPane.showMessageDialog(Account.this,"Added Success");
            }

                
            
        } catch (SQLException ex) {
            Logger.getLogger(Account.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Account.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

