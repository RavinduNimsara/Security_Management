    private void setPids() {
        try {
            pidCombo.removeAllItems();
            ArrayList<Payment> s = PaymentController.getAllPayments();
            if (s.isEmpty()) {
                return;
            }
            for (Payment p : s) {
                pidCombo.addItem(p.getPid());
            }
        } catch (SQLException ex) {
            Logger.getLogger(PaymentManage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PaymentManage.class.getName()).log(Level.SEVERE, null, ex);
        }
        new ComboSearch().setSearchableCombo(pidCombo, true, "No result found...");
    }

    private void setTextFields() {
        try {
            Payment p = null;
            try {
                p = PaymentController.getPayment(pidCombo.getSelectedItem().toString());
            } catch (NullPointerException e) {
                ridText.setText("");
                dateText.setText("");
                $Text.setText("");
                rateText.setText("");
                rsText.setText("");
                return;
            }

            ridText.setText(p.getRid());
            dateText.setText(p.getDate());
            $Text.setText(String.valueOf(p.getAmountUS$()));
            rateText.setText(String.valueOf(p.getRate()));
            rsText.setText(String.valueOf(p.getAmountRs()));

        } catch (SQLException ex) {
            Logger.getLogger(PaymentManage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PaymentManage.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void setRid() {
        try {
            ArrayList<String> r1 = ReservationController.getRids();
            for (String r : r1) {
                ridCombo.addItem(r);
            }
        } catch (SQLException ex) {
            Logger.getLogger(PaymentManage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PaymentManage.class.getName()).log(Level.SEVERE, null, ex);
        }
        new ComboSearch().setSearchableCombo(ridCombo, true, "No result found...");
    }

    private void findRs() {
        try {
            double d1 = Double.parseDouble($Text.getText());
            double d2 = Double.parseDouble(rateText.getText());

            rsText.setText(d1 * d2 + "");
        } catch (NumberFormatException numberFormatException) {

        }
    }

    private void editPayment() {
        if (rateText.getText().isEmpty() || $Text.getText().isEmpty() || dateText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(PaymentManage.this, "Enter Values");
            return;
        }
        String pid = pidCombo.getSelectedItem().toString();
        String rid = ridText.getText();
        String date = dateText.getText();
        double $ = Double.parseDouble($Text.getText());
        double rs = Double.parseDouble(rsText.getText());
        double rate = Double.parseDouble(rateText.getText());
        
        try {
            if (PaymentController.updatePayment(new Payment(pid, rid, date, $, rs, rate))) {
                JOptionPane.showMessageDialog(PaymentManage.this, "Updated Successfully");

            }
        } catch (SQLException ex) {
            Logger.getLogger(PaymentManage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PaymentManage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void delete() {
        if (pidCombo.getSelectedItem() == null) {
            return;
        }
        try {
            if (PaymentController.deletePayment(pidCombo.getSelectedItem().toString())) {
                JOptionPane.showMessageDialog(PaymentManage.this, "Deleted Successfully");
                setPids();
            }
        } catch (SQLException ex) {
            Logger.getLogger(PaymentManage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PaymentManage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
