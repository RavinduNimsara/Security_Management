    private void setTable() {
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String date = format.format(datePicker.getDate());
        model.setRowCount(0);
        try {
            ArrayList<Payment> p1 = PaymentController.getPaymentsofDate(date);
            if (p1.isEmpty()) {
                return;
            }
            for (Payment p : p1) {
                model.addRow(new Object[]{p.getPid(), p.getRid(), p.getDate(), p.getAmountUS$(), p.getAmountRs(), p.getRate()});
            }
        } catch (SQLException ex) {
            Logger.getLogger(AllPayments.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AllPayments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

