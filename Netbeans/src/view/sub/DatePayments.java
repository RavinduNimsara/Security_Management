    private void setTable2() {
        model2.setRowCount(0);
        String year=yearCombo.getSelectedItem().toString();
        String month=monthCombo.getSelectedItem().toString();
        try {
            ArrayList<Payment> p1=PaymentController.getPaymentsOfMonth(year,month);
            if (p1.isEmpty()) {
                $Total.setText("0.0");
                rsTotal.setText("0.0");
                return ;
            }
            for (Payment p : p1) {
                model2.addRow(new Object[]{p.getPid(),p.getRid(),p.getDate(),p.getAmountUS$(),p.getAmountRs(),p.getRate()});
            }
        } catch (SQLException ex) {
            Logger.getLogger(DatePayments.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DatePayments.class.getName()).log(Level.SEVERE, null, ex);
        }
        setTotal();
        
        
    }

    private void setTotal() {
        double $=0;
        double rs=0;
        for (int i = 0; i < tab2.getRowCount(); i++) {
            $+=Double.parseDouble(model2.getValueAt(i, 3).toString());
            rs+=Double.parseDouble(model2.getValueAt(i, 4).toString());
        }
        $Total.setText($+"");
        rsTotal.setText(rs+"");
        
        
        
    }
