    private void setSids() {
        try {
            ArrayList<String> s1 = SeaGuardController.getAllSids();
            if (s1.isEmpty()) {
                return;
            }
            for (String s : s1) {
                sidCombo.addItem(s);
            }
            new ComboSearch().setSearchableCombo(sidCombo, true, "No result found...");
        } catch (SQLException ex) {
            Logger.getLogger(SalariesOfSeaGuard.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SalariesOfSeaGuard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void setTable() {
        try {
            model.setRowCount(0);
            if (sidCombo.getItemCount()==0) {
                model.setRowCount(0);
                nameText.setText("");
                return ;
            }
            ArrayList<SalarySub> s1 = SalaryController.getSalariesForSid(sidCombo.getSelectedItem().toString());
            if (s1.isEmpty()) {
                model.setRowCount(0);
                nameText.setText(SeaGuardController.getSeaGuardName(sidCombo.getSelectedItem().toString()));
                return ;
            }
            for (SalarySub s : s1) {
                model.addRow(new Object[]{s.getSid(), s.getSalaryId(), s.getRid(), s.getDate(), s.getAmount()});
                
            }
            nameText.setText(s1.get(0).getName());

        } catch (SQLException ex) {
            Logger.getLogger(SalariesOfSeaGuard.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SalariesOfSeaGuard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
